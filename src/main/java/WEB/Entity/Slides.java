package WEB.Entity;

public class Slides {
	private int id;
	private String image;
	
	public Slides() {
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
}
